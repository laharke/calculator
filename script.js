//functions to operate on numbers
function add (a, b) {
    return a + b;
};

function subtract (a, b) {
    return a - b;
};

function multiply (a, b) {
    return a * b;
};

function divide (a, b){
    return a / b;
};

//operate function
function operate (operator, num1, num2) {
    return operator(num1, num2);
};

